// Embeds the shared golden-sample JSON files (fixtures/api/) into an Elm
// module for elm-test, which cannot read files. Runs as part of `pnpm test`;
// the output is gitignored. The same files back the munit golden tests on the
// Scala side.
import { mkdirSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const frontendDir = dirname(dirname(fileURLToPath(import.meta.url)));
const fixturesDir = join(frontendDir, "..", "fixtures", "api");
const outputDir = join(frontendDir, "tests", "Generated");
const outputFile = join(outputDir, "Fixtures.elm");

const entries = readdirSync(fixturesDir)
  .filter((file) => file.endsWith(".json"))
  .sort()
  .map((file) => {
    const content = readFileSync(join(fixturesDir, file), "utf8");
    if (!/^[\x00-\x7F]*$/.test(content)) {
      // JSON.stringify's \uXXXX escapes are not valid Elm; keep fixtures ASCII
      throw new Error(`${file} contains non-ASCII characters`);
    }
    const name = file.replace(/\.json$/, "");
    // JSON.stringify escaping (\" \\ \n) is valid Elm string escaping for
    // ASCII content
    return `( "${name}", ${JSON.stringify(content)} )`;
  });

const module_ = `module Generated.Fixtures exposing (fixtures)

{-| GENERATED by scripts/generate-fixtures.mjs from fixtures/api/ - do not
edit by hand.
-}

import Dict exposing (Dict)


fixtures : Dict String String
fixtures =
    Dict.fromList
        [ ${entries.join("\n        , ")}
        ]
`;

mkdirSync(outputDir, { recursive: true });
writeFileSync(outputFile, module_);
console.log(`Wrote ${entries.length} fixtures to ${outputFile}`);
